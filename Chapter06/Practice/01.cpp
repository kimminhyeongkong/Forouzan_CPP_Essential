#include <iostream>
#include <cmath>

int main()
{
  std::cout << "abs(25) = " << abs(25) << std::endl;
  std::cout << "abs(-23) = " << abs(-23) << std::endl;
  std::cout << "floor(44.56) = " << floor(44.56) << std::endl;
  std::cout << "floor(-23.78) = " << floor(-23.78) << std::endl;
  std::cout << "ceil(25.23) = " << ceil(25.23) << std::endl;
  std::cout << "ceil(-2.89) = " << ceil(-2.89) << std::endl;

  return 0;
}