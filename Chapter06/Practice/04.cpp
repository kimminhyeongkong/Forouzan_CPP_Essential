#include <iostream>
#include <cmath>

int main()
{
  std::cout << "round(23.2) = " << round(23.2) << std::endl;
  std::cout << "round(23.8) = " << round(23.8) << std::endl;
  std::cout << "round(-23.2) = " << round(-23.2) << std::endl;
  std::cout << "round(-23.8) = " << round(-23.8) << std::endl;

  return 0;
}