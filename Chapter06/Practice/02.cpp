#include <iostream>
#include <cmath>

int main()
{
  std::cout << "pow(5.0, 3) = " << pow(5.0, 3) << std::endl;
  std::cout << "pow(5, -3) = " << pow(5, -3) << std::endl;
  std::cout << "sqrt(44.56) = " << sqrt(44.56) << std::endl;
  std::cout << "exp(-6.2) = " << exp(-6.2) << std::endl;
  std::cout << "exp(44.26) = " << exp(44.26) << std::endl;
  std::cout << "log(16.2) = " << log(16.2) << std::endl;
  std::cout << "log10(14.24) = " << log10(14.24) << std::endl;

  return 0;
}