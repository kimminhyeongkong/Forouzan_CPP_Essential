#include <iostream>
#include <cmath>

int main()
{
  const double PI = 3.141592653589793238462;

  std::cout << "sin(0) = " << sin(0) << std::endl;
  std::cout << "sin(PI) = " << sin(PI) << std::endl;
  std::cout << "cos(0) = " << cos(0) << std::endl;
  std::cout << "cos(PI) = " << cos(PI) << std::endl;
  std::cout << "tan(0) = " << tan(0) << std::endl;
  std::cout << "tan(PI) = " << tan(PI) << std::endl;
  std::cout << "asin(0) = " << asin(0) << std::endl;
  std::cout << "asin(PI) = " << asin(PI) << std::endl;
  std::cout << "acos(0) = " << acos(0) << std::endl;
  std::cout << "acos(PI) = " << acos(PI) << std::endl;
  std::cout << "atan(0) = " << atan(0) << std::endl;
  std::cout << "atan(PI) = " << atan(PI) << std::endl;

  return 0;
}